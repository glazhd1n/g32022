#include <bits/stdc++.h>

using namespace std;

char a[10][10];

int main() {
    int t;
    cin >> t;
    while(t--) {
        for(int i = 1; i <= 8; i++) {
            for(int j = 1; j <= 8; j++) {
                cin >> a[i][j];
            }
        }

        for(int i = 1; i <= 8; i++) {
            for(int j = 1; j <= 8; j++) {
                if(a[i - 1][j - 1] == '#' && a[i - 1][j + 1] == '#' && a[i][j] == '#') {
                    cout << i << ' ' << j << '\n';
                }
            }
        }
    }
}



